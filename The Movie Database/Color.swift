import UIKit

//extension for some custom colors
extension UIColor {
    
    class func background() -> UIColor {
        return UIColor(red: 244/255, green: 244/255, blue: 244/255, alpha: 1)
    }
    class func blue() -> UIColor {
        return UIColor(red: 9/255, green: 28/255, blue: 36/255, alpha: 1)
    }
    class func green() -> UIColor {
        return UIColor(red: 33/255, green: 210/255, blue: 120/255, alpha: 1)
    }
    class func gray() -> UIColor {
        return UIColor(red: 42/255, green: 41/255, blue: 46/255, alpha: 1)
    }
}

